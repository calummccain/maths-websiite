function f(v) {
    return [v[0] / 4, Math.sqrt(3) * v[1] / 4, Math.sqrt(3) * v[2] / 4, Math.sqrt(3) * v[3] / 4];
}

function matrixDict(order, letter, vector) {
    var newVector;
    switch (letter) {
        case 'a':
            newVector = a(vector);
            break;
        case 'b':
            newVector = b(vector);
            break;
        case 'c':
            newVector = c(vector);
            break;
        case 'd':
            newVector = d(vector);
            break;
        case 'e':
            newVector = e(vector);
            break;
        case 'f':
            newVector = f(vector);
            break;
    }
    return newVector;
};

