function d(v) {
    return [
        (5 * v[0] - 3 * v[1] - 3 * v[2] + 3 * v[3]) / 4,
        (v[0] + v[1] - 3 * v[2] + 3 * v[3]) / 4,
        (v[0] - 3 * v[1] + v[2] + 3 * v[3]) / 4,
        (-v[0] + 3 * v[1] + 3 * v[2] + v[3]) / 4
    ];
}

