function distance(x, y) {
    var d = (x[0] - y[0]) ** 2 + (x[1] - y[1]) ** 2 + (x[2] - y[2]) ** 2 + (x[3] - y[3]) ** 2;
    return d;
}

function vectorScale(x, s) {
    var vScale = [];
    for (var i = 0; i < x.length; i++) {
        vScale[i] = x[i] * s;
    }
    return vScale;
}

